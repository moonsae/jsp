package prj0831;

public class BucketList {
	String blist;
	int age;
	public BucketList() {
	}
	public BucketList(String blist, int age) {
		super();
		this.blist = blist;
		this.age = age;
	}
	public String getBlist() {
		return blist;
	}
	public void setBlist(String blist) {
		this.blist = blist;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	

}
